    private void bm2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bm2ActionPerformed
        selected=tb.getSelectedRow();
        if(tb.isRowSelected(selected)){
            double moneySelected = Double.valueOf(tb.getValueAt(selected,3).toString().replace(",", ""));
            System.out.println("show amount-"+moneySelected);
            total-=moneySelected;
            lbtotal.setText("Price :"+df.format(total)+"Baht");
            if(total==0){
                lbtotal.setText("Price : 0.00 Baht");
            }   
            model.removeRow(selected);
        }else{
            JOptionPane.showMessageDialog(this,"click line","warning",JOptionPane.ERROR_MESSAGE);           
        }
    }//GEN-LAST:event_bm2ActionPerformed

