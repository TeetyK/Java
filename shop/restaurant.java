    private void bm1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bm1ActionPerformed
        if(t2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"enter number","warning",JOptionPane.ERROR_MESSAGE);
        }else{
        Vector v =new Vector();
        v.add(Shop.getSelectedItem().toString());
        v.add(t1.getText());
        v.add(t2.getText());
        double money=Double.valueOf(t1.getText()) * Double.valueOf(t2.getText());
        v.add(df.format(money));
        total+=money;
        model.addRow(v);
        lbtotal.setText("Price :"+df.format(total)+"Baht");
        }
    }//GEN-LAST:event_bm1ActionPerformed

